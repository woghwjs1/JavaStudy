package Chapter9;

public class ArrayIndexOutOfBoundsException {
	//ArrayIndexOutOfBoundsException이 발생하는 경우
	//배열의 범위를 초과하는 경우에 발생한다.
	public static void main(String[] args) {
		String data1 = args[0];
		String data2 = args[1];
		
		System.out.println(data1);
		System.out.println(data2);
		

	}

}
